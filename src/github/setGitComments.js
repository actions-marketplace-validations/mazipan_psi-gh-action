const github = require('@actions/github')
const { getListComments } = require('./getListComments')
const { setCommitStatus } = require('./setCommitStatus')
const { deleteComments } = require('./deleteComments')
const { blue, red } = require('../logger')

exports.setGitComments = async function setGitComments ({ commentBody, token }) {
  const context = github.context
  const octokit = github.getOctokit(token)

  const comments = await getListComments(token)
  await deleteComments(token, comments)

  try {
    blue(`> Creating new comment on commit: ${context.sha}`)

    await octokit.rest.repos.createCommitComment({
      owner: context.repo.owner,
      repo: context.repo.repo,
      commit_sha: context.sha,
      body: `${commentBody}
  <p><small><i>ðŸ‘‹ Generated by <a href="https://github.com/mazipan/psi-gh-action/" target="_blank" rel="noopenner noreferer">psi-github-action</a></i></small></p>
  <p><small><i><a href="https://github.com/mazipan/psi-gh-action#abbreviation" target="_blank" rel="noopenner noreferer">ðŸ“– Learn about the abbreviation</a></i></small></p>
      `
    })
  } catch (error) {
    red(error)
  }

  // status for current commit
  await setCommitStatus(token)
}
